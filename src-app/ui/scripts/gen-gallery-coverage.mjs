/**
 * Generate galleryCoverage.generated.ts — the DENOMINATOR of the seeded gallery
 * coverage gate: the `GallerySurface` union of every page/component id in this
 * workspace. Mirrors scripts/gen-testid-registry.mjs.
 *
 * The hand-maintained `src/dev/gallery/coverage.ts` declares
 * `GALLERY_COVERAGE satisfies Record<GallerySurface, Coverage>`, so a surface
 * with NO entry (or allow-list reason) is a COMPILE error. This generator only
 * owns the union; the Record is authored by humans.
 *
 * Surface id = path from `src/` without extension, e.g.
 *   modules/llm-provider/components/LlmProviderSettings
 *   components/ui/kit/button
 *
 * Run: node scripts/gen-gallery-coverage.mjs           (write the union)
 *      node scripts/gen-gallery-coverage.mjs --check   (drift guard, lists pending)
 *      node scripts/gen-gallery-coverage.mjs --scaffold (add missing keys to coverage.ts as pending)
 */
import { fileURLToPath } from 'node:url'
import path from 'node:path'
import fs from 'node:fs'

const HERE = path.dirname(fileURLToPath(import.meta.url))
const SRC = path.resolve(HERE, '../src')
const OUT = path.join(SRC, 'dev/gallery/galleryCoverage.generated.ts')
const COVERAGE = path.join(SRC, 'dev/gallery/coverage.ts')

// Roots whose .tsx files are visual surfaces the gallery must account for.
const ROOTS = [
  { dir: path.join(SRC, 'modules'), skipFile: f => f === 'module.tsx' },
  { dir: path.join(SRC, 'components/ui'), skipFile: () => false },
]

function walk(dir, acc = []) {
  if (!fs.existsSync(dir)) return acc
  for (const e of fs.readdirSync(dir)) {
    const full = path.join(dir, e)
    const st = fs.statSync(full)
    if (st.isDirectory()) {
      if (!['node_modules', 'dist', 'build', '.git', 'tests', '__tests__'].includes(e))
        walk(full, acc)
    } else if (/\.tsx$/.test(e) && !/\.(test|stories)\.tsx$/.test(e)) {
      acc.push(full)
    }
  }
  return acc
}

function collectSurfaces() {
  const ids = new Set()
  for (const { dir, skipFile } of ROOTS) {
    for (const f of walk(dir)) {
      if (skipFile(path.basename(f))) continue
      const rel = path.relative(SRC, f).replace(/\\/g, '/').replace(/\.tsx$/, '')
      ids.add(rel)
    }
  }
  return [...ids].sort()
}

function renderUnion(surfaces) {
  return `// AUTO-GENERATED by scripts/gen-gallery-coverage.mjs — DO NOT EDIT.
// Run \`npm run gen:gallery-coverage\` to refresh. ${surfaces.length} gallery surfaces
// (every page/component .tsx under src/modules + src/components/ui). The
// hand-maintained coverage.ts must map EVERY one of these (entry or allow-list
// reason) — a missing key is a compile error (the coverage gate).

export const GALLERY_SURFACES = [
${surfaces.map(s => `  ${JSON.stringify(s)},`).join('\n')}
] as const

/** Every visual surface (page/component) the gallery must account for. */
export type GallerySurface = (typeof GALLERY_SURFACES)[number]
`
}

const surfaces = collectSurfaces()
const body = renderUnion(surfaces)
const mode = process.argv.includes('--check')
  ? 'check'
  : process.argv.includes('--scaffold')
    ? 'scaffold'
    : 'write'

if (mode === 'check') {
  const cur = fs.existsSync(OUT) ? fs.readFileSync(OUT, 'utf-8') : ''
  if (cur.trim() !== body.trim()) {
    console.error(
      'galleryCoverage.generated.ts is stale — run `npm run gen:gallery-coverage` and commit.',
    )
    process.exit(1)
  }
  // Report (non-fatal) any surface still marked pending in coverage.ts.
  if (fs.existsSync(COVERAGE)) {
    const cov = fs.readFileSync(COVERAGE, 'utf-8')
    const pending = surfaces.filter(s =>
      new RegExp(`${JSON.stringify(s)}\\s*:\\s*pending\\(`).test(cov),
    )
    console.log(
      `galleryCoverage.generated.ts up to date (${surfaces.length} surfaces; ${pending.length} pending).`,
    )
    if (pending.length) {
      console.log('pending surfaces (tracked TODO, not a failure):')
      for (const p of pending) console.log(`  - ${p}`)
    }
  } else {
    console.log(`galleryCoverage.generated.ts up to date (${surfaces.length} surfaces).`)
  }
} else if (mode === 'scaffold') {
  // Opt-in bootstrap: append any surface missing from coverage.ts as pending().
  // NOT part of the default gen/check flow, so the tsc gate still fires first.
  if (!fs.existsSync(COVERAGE)) {
    console.error('coverage.ts does not exist yet — create it first.')
    process.exit(1)
  }
  const cov = fs.readFileSync(COVERAGE, 'utf-8')
  const missing = surfaces.filter(
    s => !new RegExp(`${JSON.stringify(s)}\\s*:`).test(cov),
  )
  if (!missing.length) {
    console.log('coverage.ts already covers every surface.')
  } else {
    const marker = '  // <<< scaffold-insert >>>'
    if (!cov.includes(marker)) {
      console.error(`coverage.ts is missing the insert marker "${marker}".`)
      process.exit(1)
    }
    const inserts = missing
      .map(s => `  ${JSON.stringify(s)}: pending('auto-scaffold — needs review'),`)
      .join('\n')
    fs.writeFileSync(COVERAGE, cov.replace(marker, `${inserts}\n${marker}`))
    console.log(`Added ${missing.length} pending surface(s) to coverage.ts.`)
  }
  fs.writeFileSync(OUT, body)
} else {
  fs.writeFileSync(OUT, body)
  console.log(`Wrote ${OUT} (${surfaces.length} surfaces).`)
}
