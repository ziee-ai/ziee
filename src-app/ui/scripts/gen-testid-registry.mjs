/**
 * Generate testIds.generated.ts — a typed registry of every STATIC data-testid literal
 * across both UI trees (ui + desktop), for agentic + i18n-safe testing.
 *
 * Why: tests must select by data-testid (visible text/labels break under i18n). This emits
 * a union of all known ids + a Playwright helper, so a test that targets a non-existent or
 * misspelled testid fails to COMPILE instead of flakily at runtime. Derived/template ids
 * (`${container}-row-${k}`) aren't enumerable statically, so the helper also accepts an
 * arbitrary string (autocompleting the known ones).
 *
 * Run: node scripts/gen-testid-registry.mjs        (write)
 *      node scripts/gen-testid-registry.mjs --check (CI drift guard)
 */
import { fileURLToPath } from 'node:url'
import path from 'node:path'
import fs from 'node:fs'

const HERE = path.dirname(fileURLToPath(import.meta.url))
const UI_SRC = path.resolve(HERE, '../src')
const DESKTOP_SRC = path.resolve(HERE, '../../desktop/ui/src')
const OUT = path.join(UI_SRC, 'components/ui/testIds.generated.ts')

const LITERAL = /data-testid\s*[=:]\s*["']([^"']+)["']/g

function walk(dir, acc = []) {
  if (!fs.existsSync(dir)) return acc
  for (const e of fs.readdirSync(dir)) {
    const full = path.join(dir, e)
    const st = fs.statSync(full)
    if (st.isDirectory()) {
      // `dev/` = the dev-only component gallery (visual-testing canvas); its
      // testids are gallery-internal and must not expand the app's typed
      // production test-id registry.
      if (!['node_modules', 'dist', 'build', '.git', 'tests', 'dev'].includes(e)) walk(full, acc)
    } else if (/\.(tsx|jsx|ts)$/.test(e)) acc.push(full)
  }
  return acc
}

const ids = new Set()
for (const root of [UI_SRC, DESKTOP_SRC]) {
  for (const f of walk(root)) {
    const src = fs.readFileSync(f, 'utf-8')
    let m
    LITERAL.lastIndex = 0
    while ((m = LITERAL.exec(src)) !== null) ids.add(m[1].trim())
  }
}
const sorted = [...ids].sort()

const body = `// AUTO-GENERATED by scripts/gen-testid-registry.mjs — DO NOT EDIT.
// Run \`npm run gen:testid-registry\` to refresh. ${sorted.length} static data-testid ids
// across the ui + desktop trees. Tests select by these (i18n-safe), with compile-time
// typo-checking via the KnownTestId union.

export const TEST_IDS = [
${sorted.map((id) => `  ${JSON.stringify(id)},`).join('\n')}
] as const

/** Every static data-testid literal in the app. Derived (\`\${id}-row-\${k}\`) ids are NOT listed. */
export type KnownTestId = (typeof TEST_IDS)[number]

/** Accepts a known id (autocompleted) OR any string (for derived/template ids). */
export type TestIdLike = KnownTestId | (string & {})

const KNOWN = new Set<string>(TEST_IDS)
/** Runtime guard — true when \`id\` is a statically-known testid. */
export const isKnownTestId = (id: string): id is KnownTestId => KNOWN.has(id)
`

const check = process.argv.includes('--check')
if (check) {
  const cur = fs.existsSync(OUT) ? fs.readFileSync(OUT, 'utf-8') : ''
  if (cur.trim() !== body.trim()) {
    console.error('testIds.generated.ts is stale — run `npm run gen:testid-registry` and commit.')
    process.exit(1)
  }
  console.log(`testIds.generated.ts up to date (${sorted.length} ids).`)
} else {
  fs.writeFileSync(OUT, body)
  console.log(`Wrote ${OUT} (${sorted.length} ids).`)
}
