/**
 * Generate fixtures/crawl.generated.ts from recorded/crawl.json.
 *
 * Emits a `crawlCassette` object literal `satisfies Cassette`, so EACH recorded
 * endpoint's response is type-checked against its api-client response type at
 * compile time (layer-1 correctness) — a drifted shape fails `tsc`. The ajv
 * contract test (check-gallery-fixtures.mjs) covers the same set vs openapi.json
 * (layer-3). Recorded from a real server (layer-2).
 *
 * Run: node scripts/gen-crawl-cassette.mjs [--check]
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const HERE = path.dirname(fileURLToPath(import.meta.url))
const UI = path.resolve(HERE, '..')
const CRAWL = path.resolve(UI, 'src/dev/gallery/fixtures/recorded/crawl.json')
const OUT = path.resolve(UI, 'src/dev/gallery/fixtures/crawl.generated.ts')

// DROP: covered by a richer hand fixture (LlmModel.list has a query resolver) —
// omitted from the crawl entirely so the hand fixture wins.
const DROP = new Set(['LlmModel.list'])

// LOOSE: recorded response is openapi-VALID (the ajv contract test passes) but
// the generated types.ts is stricter than openapi for these (a union/enum the
// structural JSON doesn't satisfy exactly). Served so their pages populate, but
// cast rather than `satisfies`-typed — the ajv contract test is their gate. Keep
// this list minimal + reviewed; re-recording from a matching binary may clear it.
const LOOSE = new Set([
  'Hub.getAssistants',
  'Hub.getCatalog',
  'Hub.getCatalogVersion',
  'Hub.getModels',
  // Carries `default_approval_mode: ApprovalMode` — a string ENUM, which a JSON
  // import widens to `string`. Same reason as the entries above.
  'Mcp.getDefaults',
  'McpServer.listAccessible',
  'McpServerSystem.list',
])

const crawl = JSON.parse(fs.readFileSync(CRAWL, 'utf8'))
const all = Object.keys(crawl).filter(k => !DROP.has(k)).sort()
const typed = all.filter(k => !LOOSE.has(k))
const loose = all.filter(k => LOOSE.has(k))

const body = `// AUTO-GENERATED by scripts/gen-crawl-cassette.mjs — DO NOT EDIT.
// Run \`npm run gen:gallery-crawl\` after re-recording. ${all.length} recorded
// paramless GET endpoints (${typed.length} typed, ${loose.length} loose).
// The typed block is \`satisfies Cassette\` so each recorded response is checked
// against its api-client response type (drift fails tsc). The loose block is
// openapi-valid (ajv contract test) but types.ts-strict-mismatched, so it's cast.
import type { Cassette } from '../mockApi'
import raw from './recorded/crawl.json'

const typed = {
${typed.map(k => `  ${JSON.stringify(k)}: raw[${JSON.stringify(k)}],`).join('\n')}
} satisfies Cassette

// openapi-valid, types.ts-strict — gated by the ajv contract test, not tsc.
const loose = {
${loose.map(k => `  ${JSON.stringify(k)}: raw[${JSON.stringify(k)}],`).join('\n')}
} as unknown as Cassette

export const crawlCassette: Cassette = { ...typed, ...loose }
`

if (process.argv.includes('--check')) {
  const cur = fs.existsSync(OUT) ? fs.readFileSync(OUT, 'utf8') : ''
  if (cur.trim() !== body.trim()) {
    console.error('crawl.generated.ts is stale — run `npm run gen:gallery-crawl` and commit.')
    process.exit(1)
  }
  console.log(`crawl.generated.ts up to date (${all.length} endpoints).`)
} else {
  fs.writeFileSync(OUT, body)
  console.log(`Wrote ${OUT} (${all.length} endpoints).`)
}
