/**
 * Vite plugin to auto-generate TypeScript types for React component names
 *
 * This plugin watches .tsx files and automatically regenerates component name types
 * whenever components are added, renamed, or deleted.
 *
 * Benefits over script approach:
 * - Auto-updates during development (no manual script runs)
 * - Integrated with Vite's watch system
 * - Types always in sync with codebase
 * - Works seamlessly with HMR
 */

import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)

// Regex patterns to match exported function components
const PATTERNS = [
  /export\s+function\s+([A-Z][a-zA-Z0-9]*)\s*\(/g,
  /export\s+const\s+([A-Z][a-zA-Z0-9]*)\s*=\s*function/g,
  /export\s+const\s+([A-Z][a-zA-Z0-9]*)\s*=\s*\([^)]*\)\s*=>/g,
  /export\s*\{\s*([A-Z][a-zA-Z0-9]*(?:\s*,\s*[A-Z][a-zA-Z0-9]*)*)\s*\}/g,
]

/**
 * Extract component names from file content
 */
function extractComponentNames(content) {
  const componentNames = new Set()

  for (const pattern of PATTERNS) {
    let match
    pattern.lastIndex = 0

    while ((match = pattern.exec(content)) !== null) {
      const captured = match[1]

      if (captured.includes(',')) {
        const names = captured.split(',').map(n => n.trim())
        names.forEach(name => {
          if (name && /^[A-Z]/.test(name)) {
            componentNames.add(name)
          }
        })
      } else if (captured && /^[A-Z]/.test(captured)) {
        componentNames.add(captured)
      }
    }
  }

  return Array.from(componentNames)
}

/**
 * Recursively find all .tsx files
 */
function findTsxFiles(dir, fileList = []) {
  if (!fs.existsSync(dir)) return fileList

  const files = fs.readdirSync(dir)

  for (const file of files) {
    const filePath = path.join(dir, file)
    const stat = fs.statSync(filePath)

    if (stat.isDirectory()) {
      if (!['node_modules', 'dist', 'build', '.git', 'tests'].includes(file)) {
        findTsxFiles(filePath, fileList)
      }
    } else if (file.endsWith('.tsx')) {
      fileList.push(filePath)
    }
  }

  return fileList
}

/**
 * Generate TypeScript file content
 */
function generateTypeFile(componentNames) {
  const sortedNames = [...componentNames].sort()

  return `/**
 * AUTO-GENERATED FILE - DO NOT EDIT MANUALLY
 *
 * Generated by vite-plugin-component-types.js
 * Automatically regenerated when .tsx files change
 *
 * Contains TypeScript types for all React component names in the codebase.
 * Used to provide IntelliSense when selecting components in tests.
 *
 * Last generated: ${new Date().toISOString()}
 */

/**
 * Union type of all available React component names in the application
 *
 * This type is used by test helpers to provide autocomplete/IntelliSense
 * when selecting components by their data-component-name attribute.
 *
 * @example
 * getComponent(page, 'UsersSettings')  // IntelliSense shows all component names
 */
export type ComponentName =
${sortedNames.map(name => `  | '${name}'`).join('\n')}

/**
 * Total number of components: ${sortedNames.length}
 */
`
}

/**
 * Scan all files and generate types
 */
function generateComponentTypes(srcDir, outputFile, logger) {
  try {
    const tsxFiles = findTsxFiles(srcDir)
    const componentMap = new Map() // Map<componentName, string[]> - tracks which files define each component

    // Collect all component names and their source files
    for (const file of tsxFiles) {
      const content = fs.readFileSync(file, 'utf-8')
      const names = extractComponentNames(content)

      for (const name of names) {
        if (!componentMap.has(name)) {
          componentMap.set(name, [])
        }
        // Store relative path for better readability
        const relativePath = path.relative(srcDir, file)
        componentMap.get(name).push(relativePath)
      }
    }

    // Detect duplicates and warn
    const duplicates = []
    for (const [name, files] of componentMap.entries()) {
      if (files.length > 1) {
        duplicates.push({ name, files })
      }
    }

    if (duplicates.length > 0) {
      logger.warn(`\n[component-types] ⚠️  Found ${duplicates.length} duplicate component name(s):`)
      for (const { name, files } of duplicates) {
        logger.warn(`  • "${name}" defined in:`)
        files.forEach(file => logger.warn(`    - ${file}`))
      }
      logger.warn('\n[component-types] Duplicate names may cause ambiguous component selection in tests.\n')

      // Uncomment to fail build on duplicates:
      // throw new Error(`Duplicate component names found: ${duplicates.map(d => d.name).join(', ')}`)
    }

    const allComponentNames = new Set(componentMap.keys())
    const typeContent = generateTypeFile(allComponentNames)

    // Ensure output directory exists
    const outputDir = path.dirname(outputFile)
    if (!fs.existsSync(outputDir)) {
      fs.mkdirSync(outputDir, { recursive: true })
    }

    // Only write if content changed (avoid unnecessary HMR)
    let shouldWrite = true
    if (fs.existsSync(outputFile)) {
      const existingContent = fs.readFileSync(outputFile, 'utf-8')
      // Compare without timestamp
      const existingWithoutTimestamp = existingContent.replace(/Last generated:.*/, '')
      const newWithoutTimestamp = typeContent.replace(/Last generated:.*/, '')
      shouldWrite = existingWithoutTimestamp !== newWithoutTimestamp
    }

    if (shouldWrite) {
      fs.writeFileSync(outputFile, typeContent, 'utf-8')
      logger.info(`[component-types] Generated types for ${allComponentNames.size} components`)
    }

    return allComponentNames.size
  } catch (error) {
    logger.error('[component-types] Failed to generate types:', error)
    return 0
  }
}

/**
 * Vite plugin
 */
export function componentTypesPlugin(options = {}) {
  const {
    srcDir = 'src',
    outputFile = 'tests/helpers/component-names.generated.ts',
  } = options

  let config
  let srcPath
  let outputPath

  return {
    name: 'vite-plugin-component-types',

    configResolved(resolvedConfig) {
      config = resolvedConfig
      // config.root is already pointing to 'src', so we use the parent directory
      const rootDir = path.dirname(config.root)
      srcPath = path.resolve(rootDir, srcDir)
      outputPath = path.resolve(rootDir, outputFile)

      // Generate on plugin init
      generateComponentTypes(srcPath, outputPath, config.logger)
    },

    handleHotUpdate({ file, server }) {
      // Regenerate when .tsx files change
      if (file.endsWith('.tsx') && !file.includes('node_modules')) {
        // Debounce regeneration (avoid multiple triggers)
        clearTimeout(this._debounceTimer)
        this._debounceTimer = setTimeout(() => {
          const count = generateComponentTypes(srcPath, outputPath, config.logger)
          if (count > 0) {
            // Trigger HMR for modules that import component-names
            const module = server.moduleGraph.getModuleById(outputPath)
            if (module) {
              server.moduleGraph.invalidateModule(module)
              server.ws.send({
                type: 'full-reload',
                path: '*'
              })
            }
          }
        }, 500)
      }
    },

    buildStart() {
      // Regenerate at build start
      generateComponentTypes(srcPath, outputPath, config.logger)
    },
  }
}
